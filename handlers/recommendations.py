            f"<b>{title}</b>\n"
            f"üõ† <b>–†–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫:</b> {developer}\n"
            f"üè¢ <b>–ò–∑–¥–∞—Ç–µ–ª—å:</b> {publisher}\n"
            f"üìÖ <b>–î–∞—Ç–∞ —Ä–µ–ª–∏–∑–∞:</b> {release_date}\n"
            f"üéÆ <b>–ñ–∞–Ω—Ä:</b> {genre}\n"
