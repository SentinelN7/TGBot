        if slug:
            keyboard_buttons.append([InlineKeyboardButton(text="–ü–æ–¥—Ä–æ–±–Ω–µ–µ", url=f"https://rawg.io/games/{slug}")])
        keyboard_buttons.append([InlineKeyboardButton(text="–î–æ–±–∞–≤–∏—Ç—å –≤ –∏–∑–±—Ä–∞–Ω–Ω–æ–µ", callback_data=f"favorite_{game_id}")])
